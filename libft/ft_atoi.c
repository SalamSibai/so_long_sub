#include "libft.h"

int	ft_atoi(const char *str)
{
	int					i;
	int					sgn;
	unsigned long long	num;

	i = 0;
	num = 0;
	sgn = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sgn = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		num = num * 10 + (str[i++] - '0');
	if (num >= __LONG_LONG_MAX__)
		return (-(sgn == 1));
	return (num * sgn);
}
